int init_setting(t_mlx *mlx)
{
    mlx->mlx_ptr = mlx_init();
    mlx->win_ptr = mlx_new_window(mlx->mlx_ptr, WIN_WIDTH, WIN_HEIGHT, "DomMorello");
    mlx->img.img_ptr = mlx_new_image(mlx->mlx_ptr, WIN_WIDTH, WIN_HEIGHT);
    mlx->img.data = (int *)mlx_get_data_addr(mlx->img.img_ptr, &mlx->img.bpp, &mlx->img.size_l, &mlx->img.endian);
    mlx->game.posX = 100;
    return 0;
}

int key_press(int key, t_mlx *mlx)
{
    static int flag = 1;
    printf("key: %d\n", key);
    mlx_destroy_image(mlx->mlx_ptr, mlx->img.img_ptr);
    mlx->img.img_ptr = mlx_new_image(mlx->mlx_ptr, WIN_WIDTH, WIN_HEIGHT);
    mlx->img.data = (int *)mlx_get_data_addr(mlx->img.img_ptr, &mlx->img.bpp, &mlx->img.size_l, &mlx->img.endian);

    if (flag++ % 2 == 1)
        mlx->game.posX = 400;
    else
        mlx->game.posX = 200;
    return 0;
}

int run_game(t_mlx *mlx)
{
    for (int i = 0; i < 600; i++)
    {
        mlx->img.data[(int)(mlx->game.posX) + WIN_WIDTH * i] = 0xFFFFFF;
    }
    mlx_put_image_to_window(mlx->mlx_ptr, mlx->win_ptr, mlx->img.img_ptr, 0, 0);
}

int start_all(t_mlx *mlx)
{
    init_setting(mlx);
    mlx_hook(mlx->win_ptr, 2, 1L<<0, key_press, mlx);
    mlx_loop_hook(mlx->mlx_ptr, run_game, mlx);
    mlx_loop(mlx->mlx_ptr);
    return 0;
}

int main(int argc, char const *argv[])
{
    t_mlx mlx;

    start_all(&mlx);
    return 0;
}
